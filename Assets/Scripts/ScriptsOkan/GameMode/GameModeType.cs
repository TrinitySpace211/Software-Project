public enum GameModeType {
    None,          // Standard - keine Einschraenkung
    OnlySprinter,  // Es spawnen nur Sprinter-Zombies
    OnlyTanks,     // Es spawnen nur Tank-Zombies
    EasyMode,      // Es spawnen nur normale Zombies
    MeleeOnly,     // Spieler hat nur Nahkampfwaffen (keine Schusswaffen/Munition)
    PistolMelee    // Spieler hat nur Pistole + Nahkampfwaffe
}
